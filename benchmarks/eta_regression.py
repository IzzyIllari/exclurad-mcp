"""Benchmark leg A: eta regression (see docs/benchmark-plan.md).

Re-runs a stratified sample of the hand-validated October 2025 chunks
through the server pipeline (points -> render_input -> run -> radtot) and
compares against the recorded outputs, chunk grouping held fixed.

Two comparison tiers, scored separately:
  - bit-identity per row (expected only when reference and rerun share
    platform + compiler; the October reference is a JLab-farm Linux build);
  - numeric agreement within tolerances far below the integrator's own
    ot = 1e-3 setting (the cross-platform criterion).

Also checks that inputs regenerated by inputgen are Fortran-equivalent to
the originals preserved in the October tree (token-wise float equality;
the original generator wrote cosmetic quirks like a duplicated W line).

Usage:
  PYTHONPATH=src python3 benchmarks/eta_regression.py \
      --source .../results_Oct2025/outputs/first \
      --work /path/to/eta-checkout/exclurad \
      --outdir benchmarks/<date>-eta-regression \
      [--n-chunks 50] [--workers 6] [--seed 20260721]
"""

import argparse
import json
import os
import random
import re
import shutil
import time
from concurrent.futures import ThreadPoolExecutor
from pathlib import Path

from exclurad_mcp.channels import get_channel
from exclurad_mcp.inputgen import InputHeader, render_input
from exclurad_mcp.runner import run_input_file
from exclurad_mcp.validators import KinematicPoint

# |new - old| ceilings per radtot column (absolute). Kinematic echo columns
# must be exact. delta/sigma ceilings sit >3 orders below ot = 1e-3; the
# asym_rc_pct ratio is skipped at phi = 0/180/360 where the schema documents
# it as meaningless (A_Born ~ 0) and gets a ratio-amplified ceiling elsewhere.
TOL = {"delta": 5e-7, "sigma_born": 1e-7,
       "asym_obs_pct": 1e-4, "asym_born_pct": 1e-4, "asym_rc_pct": 1e-2}
COLS = ["e1", "w", "q2", "cos_theta", "phi_deg",
        "delta", "sigma_born", "asym_obs_pct", "asym_born_pct", "asym_rc_pct"]
KIN = {"e1", "w", "q2", "cos_theta", "phi_deg"}
NAME_RE = re.compile(r"W(?P<w>\d+\.\d+)_Q2(?P<q2>\d+\.\d+)")


def parse_original_input(path: Path) -> tuple[list[KinematicPoint], list[str]]:
    """Extract the 10 points and header tokens the Fortran actually reads."""
    lines = path.read_text().splitlines()
    header_vals = [ln.split("!")[0].strip() for ln in lines[:7]]
    n_idx = next(i for i, ln in enumerate(lines) if "no. of points" in ln)
    n = int(lines[n_idx].split("!")[0])
    axes = []
    for off in range(1, 5):
        toks = lines[n_idx + off].split("!")[0].split()
        axes.append([float(t) for t in toks[:n]])
    pts = [KinematicPoint(w=axes[0][i], q2=axes[1][i],
                          cos_theta=axes[2][i], phi=axes[3][i]) for i in range(n)]
    return pts, header_vals


def data_rows(text: str) -> list[list[float]]:
    rows = []
    for ln in text.splitlines():
        if ln.strip() and "," in ln:
            rows.append([float(f) for f in ln.split(",")])
    return rows


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--source", required=True)
    ap.add_argument("--work", required=True, help="eta checkout dir (build/ + .tbl)")
    ap.add_argument("--outdir", required=True)
    ap.add_argument("--n-chunks", type=int, default=50)
    ap.add_argument("--workers", type=int, default=6)
    ap.add_argument("--seed", type=int, default=20260721)
    ap.add_argument("--timeout-sec", type=int, default=1800)
    args = ap.parse_args()

    rng = random.Random(args.seed)
    # absolute paths throughout: the runner execs the exe with cwd set to the
    # worker dir, so relative paths would resolve against the wrong root
    source, work = Path(args.source).resolve(), Path(args.work).resolve()
    outdir = Path(args.outdir).resolve()
    scratch = outdir / "scratch"
    shutil.rmtree(scratch, ignore_errors=True)

    # ---- candidate chunks: original input + recorded radtot, no recorded NaN ----
    candidates = []
    n_nan_skipped = 0
    for rad in sorted(source.rglob("radtot/*.dat")):
        inp = rad.parent.parent / "inputs" / rad.name
        if not inp.exists():
            continue
        if "nan" in rad.read_text().lower():
            n_nan_skipped += 1
            continue
        candidates.append((inp, rad))

    # ---- stratify: spread the sample across distinct (W, Q2) cells ----
    rng.shuffle(candidates)
    picked, seen_cells = [], set()
    for inp, rad in candidates:
        m = NAME_RE.search(rad.name)
        cell = m.group("w", "q2") if m else rad.name
        if cell in seen_cells:
            continue
        seen_cells.add(cell)
        picked.append((inp, rad))
        if len(picked) == args.n_chunks:
            break
    for inp, rad in candidates:  # top up if fewer cells than chunks
        if len(picked) == args.n_chunks:
            break
        if (inp, rad) not in picked:
            picked.append((inp, rad))

    ch = get_channel("eta")
    exe = work / "build" / "exclurad"

    header = InputHeader.for_channel(ch)  # eta defaults: model 3, bmom 6.53, vcut 0.166

    def run_one(idx_pair):
        idx, (orig_inp, ref_rad) = idx_pair
        # one work dir per CHUNK: pool.map assigns tasks to whichever thread
        # is free, so worker-indexed dirs would race and clobber outputs
        wd = scratch / f"chunk_{idx:03d}"
        wd.mkdir(parents=True)
        os.symlink(exe, wd / "exclurad")
        os.symlink(work / ch.table_file, wd / ch.table_file)
        pts, header_vals = parse_original_input(orig_inp)
        # input equivalence: header floats + the 10 values per axis
        gen_text = render_input(header, pts)
        (wd / f"gen_{idx:03d}.dat").write_text(gen_text)
        rpts, rheader = parse_original_input(wd / f"gen_{idx:03d}.dat")
        input_equiv = (
            [float(v) for v in rheader] == [float(v) for v in header_vals]
            and all(
                (a.w, a.q2, a.cos_theta, a.phi) == (b.w, b.q2, b.cos_theta, b.phi)
                for a, b in zip(rpts, pts)
            )
        )
        t0 = time.time()
        out = run_input_file(wd / "exclurad", wd / f"gen_{idx:03d}.dat",
                             wd, wd / "results", timeout_sec=args.timeout_sec)
        elapsed = round(time.time() - t0, 1)
        rec = {"chunk": ref_rad.name, "status": out.status,
               "input_equivalent": input_equiv, "elapsed_sec": elapsed}
        if out.status != "OK":
            return rec
        new_rows = data_rows(Path(out.outputs["radtot"]).read_text())
        old_rows = data_rows(ref_rad.read_text())
        new_text = [ln for ln in Path(out.outputs["radtot"]).read_text().splitlines() if ln.strip()]
        old_text = [ln for ln in ref_rad.read_text().splitlines() if ln.strip()]
        rec["n_rows"] = len(new_rows)
        rec["rows_match_count"] = len(new_rows) == len(old_rows)
        rec["bit_identical_rows"] = sum(1 for a, b in zip(old_text, new_text) if a == b)
        maxdev = {}
        kin_exact = True
        n_rc_skipped = 0
        for o, n in zip(old_rows, new_rows):
            phi_degenerate = o[4] % 180.0 == 0.0
            for ci, col in enumerate(COLS):
                d = abs(o[ci] - n[ci])
                if col in KIN:
                    kin_exact = kin_exact and d == 0.0
                elif col == "asym_rc_pct" and phi_degenerate:
                    n_rc_skipped += 1
                else:
                    maxdev[col] = max(maxdev.get(col, 0.0), d)
        rec["kinematics_exact"] = kin_exact
        rec["asym_rc_rows_skipped_degenerate_phi"] = n_rc_skipped
        rec["max_abs_dev"] = {k: float(f"{v:.3e}") for k, v in maxdev.items()}
        rec["within_tolerance"] = (
            rec["rows_match_count"] and len(new_rows) > 0 and kin_exact
            and all(maxdev.get(c, 0.0) <= t for c, t in TOL.items())
        )
        return rec

    with ThreadPoolExecutor(max_workers=args.workers) as pool:
        records = list(pool.map(run_one, enumerate(picked)))

    ok = [r for r in records if r["status"] == "OK"]
    results = {
        "seed": args.seed,
        "source": str(source),
        "n_chunks": len(picked),
        "n_candidates": len(candidates),
        "n_nan_chunks_excluded": n_nan_skipped,
        "distinct_wq2_cells": len(seen_cells),
        "tolerances_abs": TOL,
        "summary": {
            "run_ok": len(ok),
            "input_equivalent": sum(1 for r in records if r["input_equivalent"]),
            "within_tolerance": sum(1 for r in ok if r.get("within_tolerance")),
            "kinematics_exact": sum(1 for r in ok if r.get("kinematics_exact")),
            "bit_identical_rows": sum(r.get("bit_identical_rows", 0) for r in ok),
            "total_rows": sum(r.get("n_rows", 0) for r in ok),
            "max_abs_dev_overall": {
                c: float(f"{max((r['max_abs_dev'].get(c, 0.0) for r in ok), default=0.0):.3e}")
                for c in TOL
            },
            "total_wall_sec": round(sum(r["elapsed_sec"] for r in records), 1),
        },
        "chunks": records,
    }
    outdir.mkdir(parents=True, exist_ok=True)
    (outdir / "results.json").write_text(json.dumps(results, indent=2))
    shutil.rmtree(scratch, ignore_errors=True)
    print(json.dumps({k: results[k] for k in
                      ("n_chunks", "n_nan_chunks_excluded", "summary")}, indent=2))


if __name__ == "__main__":
    main()
